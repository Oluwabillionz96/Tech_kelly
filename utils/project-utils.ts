export const getColorClasses = (currentCategory: { color: string }) => {
  switch (currentCategory.color) {
    case "emerald":
      return {
        bg: "bg-primary/10",
        text: "text-primary",
        hover: "hover:text-primary",
      };
    case "blue":
      return {
        bg: "bg-blue-500/10",
        text: "text-blue-500",
        hover: "hover:text-blue-500",
      };
    case "purple":
      return {
        bg: "bg-purple-500/10",
        text: "text-purple-500",
        hover: "hover:text-purple-500",
      };
    default:
      return {
        bg: "bg-primary/10",
        text: "text-primary",
        hover: "hover:text-primary",
      };
  }
};
